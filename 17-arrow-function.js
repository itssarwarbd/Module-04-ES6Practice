// Arrow Function 

var myArrow=()=>{
    console.log("My First Arrow Function");
}
myArrow();

// Arrow Function of Anonymous FUnction 

var myArrow=(...x)=>{
    console.log(x);
}
myArrow(1,2,3,4,5);

// Arrow Function of Return FUnction 

var myArrow=()=>{
    console.log("Hello JavaScript");
}
myArrow();
